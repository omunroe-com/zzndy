    <h1> Ласкаво просимо </h1>
    <p>до офіційного сайту, присвяченого житті та діяльності Миколи Миколайовича  Щербака (31.Х1927  - 27.I 1998), доктора  біологічних наук, професора, заслуженого діяча науки, чл.-кор. НАН України.</p>
    <p>      Щербак М.М. один з  провідних зоологів України. Його наукові праці, присвячені систематиці,  екології та зоогеографії хребетних тварин (зокрема рептилій та амфібій) широко  відомі зоологам всього світу. За цикл робіт з герпетології він удостоений  Премії ім. Д.К Заболотного. Під його керівництвом здійснено більше 44  експедицій, в тому числі у віддалені райони Середньої та Центральної Азії,  Сибіру, Далекого Сходу, Курильських островів, Туви, Монголії, що збагатили як  експозицію, так і фонди Зоологічного музею, з якими працюють фахівці багатьох  країн.<br />
      <br />
      Щербак М.М. - один з ініціаторів  створення  Національного  науково-природничого музею НАН України, під його керівництвом та при  безпосередній участі збудовано нинішній Зоологічний музей.<br />
      <br />
      Він засновник української школи  систематиків-герпетологів, автор та спіавтор близько 300 наукових та  науково-популярних книг. М.М. Щербак розробив загальноприйняту зараз систему  палеарктичних геконів, ящурок та аблефаридних ящірок, сформулював  циркумевксинську гіпотезу походження герпетофауни Криму, обґрунтував наявність  північноафриканського, середньоазійського та центральноазійського центрів  походження пустельної герпетофауни, розробив герпетогеографічне районування  Палеаркиики та інше.<br />
      <br />
      Вагомим є внесок М.М. Щербака в  охорону природи. За його участю створювалась Червона книга СРСР (1 вид. 1978, 2  вид. 1984), а в 1994 році під його керівництвом видана Червона книга України  (тваринний світ).<br />
    Щербак М.М. приймав активну участь у  громадському житті України, плідно співпрацював з закордонними колегами, був  одним з авторів довідника по рептиліях і амфібіях Европи.</p>
